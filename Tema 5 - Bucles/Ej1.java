/**
 *
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle for
 *
 * @author Francisco Javier Reina Benítez
 *
 * Ejercicio 1 Tema 5
 */

public class Ej1 {

  public static void main(String[] args) {
    
    for (int i = 0; i <= 100; i += 5) {
      System.out.println(i);
    }
  }
}
